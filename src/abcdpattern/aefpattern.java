package abcdpattern;

public class aefpattern {

	public static void main(String[] args) {
		        int  i = 1;
		        char n = 'a';

		        while (i <= 5) {
		            int j = 1;
		            while (j<= i) {
		                System.out.print(n);
		                n++;
		                j++;
		            }
		            System.out.println();
		            i++;
		    }
		}
}
		